package 변수_조건_반복;

public class LongExample {

    public static void main ( String[] args){

        long var1 = 10;
        long var2 = 20L;
        long var3 = 100000000000000000L;
        //  int 타입의 저장 범위를 넘어서는 정수 리터럴에 'L'을 붙여줘야한다.

        System.out.println(var1);
        System.out.println(var2);
        System.out.println(var3);

    }
}
